package Lecture1;

public class Variables {
    public static void main(String[] args) {
        // Declaration
        int myNumber = 18;
        // initialization
        int yourNumber;
        yourNumber = 865;

        System.out.println("myNumber: " + myNumber);
        System.out.println("yourNumber: " + yourNumber);

        boolean flag = true;
        System.out.println("flag: " + flag);

        String wishes = "Good Morning";

        System.out.println("Wish: " + wishes);

        // any value we are asigning is called literal
        // like string literal, int literal, boolean literal ... etc.

        char myCharecter = 'k';

        System.out.println("Charecter: " + myCharecter);

    }
}
